package com.challenges.solutions.pointsandlines;

public class Point {
    //Since there is no info on these point and line classes I will just assume what I think they have
    private int xPos;
    private int yPos;

    public Point(int xPos, int yPos) {
        this.xPos = xPos;
        this.yPos = yPos;
    }

    public int getxPos() {
        return xPos;
    }

    public int getyPos() {
        return yPos;
    }

}
